"""# Stage 8: CNN Branch - MobileNetV2"""

cnn_input = layers.Input(shape=(*IMG_SIZE, 3), name="cnn_input")

# ImageNet pretrained - 1.4M images already learned!
mobilenet = MobileNetV2(
    input_shape=(*IMG_SIZE, 3),
    include_top=False,    # Remove final classifier layer
    weights="imagenet"    # Use pretrained weights!
)

# Phase 1: Freeze - don't change ImageNet knowledge yet
mobilenet.trainable = False

# Feature map extract
cnn_features_map = mobilenet(cnn_input, training=False)
# Output shape: (4, 4, 1280)
# 4x4 spatial positions, 1280 feature channels

# Reshape: 4x4 = 16 spatial tokens
n_spatial  = 4 * 4  # = 16
cnn_tokens = layers.Reshape(
    (n_spatial, 1280),
    name="cnn_tokens"
)(cnn_features_map)
# Now: (16, 1280)

# Project 1280 → 64 (match ViT dimension!)
cnn_projected = layers.Dense(
    EMBED_DIM,           # 64
    activation="relu",
    name="cnn_projection"
)(cnn_tokens)
# Final: (16, 64) ✅

cnn_branch = models.Model(
    cnn_input,
    cnn_projected,
    name="CNN_MobileNetV2_Branch"
)
print(f"CNN Branch output: {cnn_branch.output_shape}")
# (None, 16, 64)

"""#Stage 9: ViT Branch

"""

VIT_PATCHES = (IMG_SIZE[0] // PATCH_SIZE) ** 2
# = (128 // 8)² = 16² = 256 patches

vit_input = layers.Input(shape=(*IMG_SIZE, 3), name="vit_input")

# Step 1: Patch Embedding
# Conv2D with stride=patch_size creates patches automatically!
x = layers.Conv2D(
    filters=EMBED_DIM,   # 64
    kernel_size=PATCH_SIZE,  # 8x8 patches
    strides=PATCH_SIZE,      # Non-overlapping
    padding="valid",
    name="patch_embedding"
)(vit_input)
# Output: (16, 16, 64)

# Step 2: Reshape to sequence
x = layers.Reshape(
    (VIT_PATCHES, EMBED_DIM),  # (256, 64)
    name="patch_sequence"
)(x)

# Step 3: Positional Encoding
# ViT-ku position sense illai by default!
positions    = tf.range(start=0, limit=VIT_PATCHES, delta=1)
pos_encoding = layers.Embedding(
    input_dim=VIT_PATCHES,   # 256 positions
    output_dim=EMBED_DIM     # 64 dim each
)(positions)
x = x + pos_encoding  # Add position info to patches

# Step 4: Transformer Blocks (x2)
for i in range(VIT_BLOCKS):  # 2 blocks

    # Self-Attention: Every patch attends to every other patch
    attn = layers.MultiHeadAttention(
        num_heads=NUM_HEADS,                    # 2 heads
        key_dim=EMBED_DIM // NUM_HEADS,         # 32 per head
        name=f"self_attn_{i}"
    )(x, x)  # Query=x, Key=x, Value=x (self attention!)

    # Residual connection
    x = layers.Add()([x, attn])
    x = layers.LayerNormalization()(x)

    # Feed Forward Network
    ffn = layers.Dense(EMBED_DIM * 2, activation="relu")(x)  # 128
    ffn = layers.Dense(EMBED_DIM)(ffn)  # back to 64
    x = layers.Add()([x, ffn])
    x = layers.LayerNormalization()(x)

vit_branch = models.Model(vit_input, x, name="ViT_Branch")
print(f"ViT Branch output: {vit_branch.output_shape}")
# (None, 256, 64)

"""#Stage 10: Cross-Attention Fusion"""

hybrid_input = layers.Input(shape=(*IMG_SIZE, 3), name="hybrid_input")

# Same image, two different branches
cnn_features = cnn_branch(hybrid_input)  # (16, 64)  - local
vit_features = vit_branch(hybrid_input)  # (256, 64) - global

# Cross-Attention!
# CNN asks questions (Query)
# ViT provides answers (Key, Value)
cross_attn = layers.MultiHeadAttention(
    num_heads=NUM_HEADS,
    key_dim=EMBED_DIM // NUM_HEADS,
    name="cross_attention"
)(
    query=cnn_features,   # CNN: "what's important here?"
    key=vit_features,     # ViT: "here's the global context"
    value=vit_features    # ViT: "here's the information"
)

# Residual + Norm
fusion = layers.Add(name="cross_residual")([cnn_features, cross_attn])
fusion = layers.LayerNormalization(name="cross_norm")(fusion)

# Pool and Dense
fusion_pooled = layers.GlobalAveragePooling1D(
    name="fusion_pool"
)(fusion)
# (16, 64) → (64,)

fusion_output = layers.Dense(
    256, activation="relu", name="fusion_dense"
)(fusion_pooled)
# (256,)

fusion_output = layers.Dropout(0.4)(fusion_output)
# Dropout - prevent overfitting

"""#Stage 11: Output Heads

"""

# Head 1: Disease Classification
disease_output = layers.Dense(
    NUM_CLASSES,          # 11 classes
    activation="softmax", # Probabilities sum to 1
    dtype="float32",      # Mixed precision override
    name="disease_output"
)(fusion_output)
# Output: [0.02, 0.85, 0.01, ...] → argmax → "Late Blight"

# Head 2: Severity Estimation
sev = layers.Dense(64, activation="relu")(fusion_output)
severity_output = layers.Dense(
    1,
    activation="sigmoid", # 0 to 1 range
    dtype="float32",
    name="severity_output"
)(sev)
# Output: 0.85 → "85% severe!"

# Final Model
hybrid_model = models.Model(
    inputs=hybrid_input,
    outputs=[disease_output, severity_output],
    name="Hybrid_MobileNetV2_ViT"
)

hybrid_model.summary()

"""#Stage 12: Custom Generator"""

def multi_output_generator(generator, class_to_severity):
    while True:
        images, labels = next(generator)

        # Class index from one-hot labels
        class_indices = np.argmax(labels, axis=1)

        # Map each class → severity score
        severity_labels = np.array(
            [class_to_severity[idx] for idx in class_indices],
            dtype=np.float32
        ).reshape(-1, 1)
        # Shape: (32, 1)

        yield images, {
            "disease_output": labels,         # One-hot
            "severity_output": severity_labels # Severity score
        }

train_multi_gen = multi_output_generator(
    train_generator,
    class_index_to_severity
)
valid_multi_gen = multi_output_generator(
    valid_generator,
    class_index_to_severity
)

"""#Stage 13: Phase 1 Training"""

def get_callbacks(phase):
    return [
        # Stop if no improvement
        EarlyStopping(
            monitor="val_disease_output_accuracy",
            mode="max",
            patience=4,          # 4 epochs wait
            restore_best_weights=True
        ),
        # Reduce LR if loss stuck
        ReduceLROnPlateau(
            monitor="val_loss",
            mode="min",
            factor=0.5,          # LR halve pannu
            patience=2,
            min_lr=1e-7
        ),
        # Save best model
        ModelCheckpoint(
            f"best_model_phase{phase}.keras",
            monitor="val_disease_output_accuracy",
            mode="max",
            save_best_only=True
        )
    ]

# Compile
