"""# Stage 18: Grad-CAM"""

# Find last Conv2D in MobileNetV2
all_conv_layers = [
    l for l in mobilenet.layers
    if isinstance(l, tf.keras.layers.Conv2D)
]
last_conv_layer = all_conv_layers[-1]

# Grad-CAM model
grad_model = tf.keras.models.Model(
    inputs=mobilenet.input,
    outputs=[last_conv_layer.output, mobilenet.output]
)

def make_gradcam_heatmap(img_array):
    with tf.GradientTape() as tape:
        conv_out, preds = grad_model(img_array, training=False)
        class_ch = tf.reduce_max(preds, axis=-1)

    # Gradients
    grads  = tape.gradient(class_ch, conv_out)
    pooled = tf.reduce_mean(grads, axis=(0, 1, 2))

    # Weighted sum
    heatmap = tf.reduce_sum(conv_out[0] * pooled, axis=-1)
    heatmap = tf.maximum(heatmap, 0)
    heatmap = heatmap / (tf.reduce_max(heatmap) + 1e-8)

    return heatmap.numpy()

# Generate heatmap
original = img_to_array(
    load_img(image_path, target_size=IMG_SIZE)
).astype("uint8")

heatmap   = make_gradcam_heatmap(img_array)
heatmap   = np.squeeze(heatmap).astype("float32")
W, H      = IMG_SIZE[1], IMG_SIZE[0]
heatmap_r = np.uint8(255 * cv2.resize(heatmap, (W, H)))
